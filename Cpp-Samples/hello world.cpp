#include <iostream> /*pre-processor directive, this one includes file*/

using namespace std;/*std stands for standard library, that means we will be using all the standard stuff nothing fancy*/

int main()
{
    cout << "Hello World!" << endl; /*this is basically how you print out something on the screen*/
    return 0;
}
/* cout is an output stream object,
*it always uses with "<<" and that means stream insertion operator,
*it basically takes everything from cout and prints it on the screen
*endl means end line or go to the next line
*/
